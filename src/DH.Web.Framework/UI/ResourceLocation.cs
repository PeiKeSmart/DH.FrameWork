namespace DH.Web.Framework.UI
{
    /// <summary>
    /// 资源位置
    /// </summary>
    public enum ResourceLocation
    {
        /// <summary>
        /// 固定到声明位置，但如果启用，则移动到绑定。
        /// </summary>
        Auto,
        /// <summary>
        /// 固定在申报地点
        /// </summary>
        None,
        /// <summary>
        /// 头部
        /// </summary>
        Head,
        /// <summary>
        /// 页脚
        /// </summary>
        Footer,
    }
}
